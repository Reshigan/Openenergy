CREATE TABLE IF NOT EXISTS `oe_ipp_refinancing` (
  id TEXT PRIMARY KEY,
  participant_id TEXT NOT NULL,
  project_id TEXT NOT NULL,
  debt_quantum_zar REAL NOT NULL,
  refinancing_tier TEXT NOT NULL CHECK(refinancing_tier IN ('minor','moderate','significant','major','material')),
  refinancing_type TEXT CHECK(refinancing_type IN ('term_loan_refinancing','bond_issuance','green_bond','refinancing_with_equity_release','debt_restructuring','lender_substitution')),
  lender_name TEXT,
  sarb_approval_required INTEGER NOT NULL DEFAULT 0,
  description TEXT,
  chain_status TEXT NOT NULL DEFAULT 'refinancing_mandated' CHECK(chain_status IN ('refinancing_mandated','term_sheet_signed','credit_approval','conditions_precedent','sarb_exchange_control','nersa_clearance','legal_documentation','financial_close','abandoned','rejected','lender_default','recovery_in_progress')),
  sla_due_at TEXT,
  sla_breached INTEGER NOT NULL DEFAULT 0,
  financial_close_at TEXT,
  actual_debt_zar REAL,
  rejected_at TEXT,
  lender_default_at TEXT,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_ipp_refinancing_participant
  ON oe_ipp_refinancing(participant_id);

CREATE INDEX IF NOT EXISTS idx_ipp_refinancing_project
  ON oe_ipp_refinancing(project_id);

CREATE INDEX IF NOT EXISTS idx_ipp_refinancing_chain_status
  ON oe_ipp_refinancing(chain_status);

CREATE INDEX IF NOT EXISTS idx_ipp_refinancing_sla_due_at
  ON oe_ipp_refinancing(sla_due_at)
  WHERE sla_breached = 0;

