CREATE TABLE IF NOT EXISTS `oe_ipp_fm` (
  id TEXT PRIMARY KEY,
  participant_id TEXT NOT NULL,
  project_id TEXT NOT NULL,
  fm_category TEXT CHECK(fm_category IN ('weather_event','grid_failure','natural_disaster','regulatory_change','pandemic','war_civil_unrest','supplier_fm')),
  lost_generation_mwh REAL NOT NULL,
  fm_tier TEXT NOT NULL CHECK(fm_tier IN ('minor','moderate','significant','major','material')),
  description TEXT,
  event_date TEXT,
  chain_status TEXT NOT NULL DEFAULT 'fm_event_occurred' CHECK(chain_status IN ('fm_event_occurred','fm_notice_issued','fm_notice_verified','fm_relief_in_progress','fm_monitoring','fm_resolved','fm_disputed','fm_arbitration','fm_arbitration_determined','fm_prolonged_termination','withdrawn')),
  sla_due_at TEXT,
  sla_breached INTEGER NOT NULL DEFAULT 0,
  notice_issued_at TEXT,
  notice_verified_at TEXT,
  relief_granted_at TEXT,
  resolved_at TEXT,
  arbitration_determined_at TEXT,
  prolonged_declared_at TEXT,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_ipp_fm_participant
  ON oe_ipp_fm(participant_id);

CREATE INDEX IF NOT EXISTS idx_ipp_fm_project
  ON oe_ipp_fm(project_id);

CREATE INDEX IF NOT EXISTS idx_ipp_fm_chain_status
  ON oe_ipp_fm(chain_status);

CREATE INDEX IF NOT EXISTS idx_ipp_fm_sla_due_at
  ON oe_ipp_fm(sla_due_at)
  WHERE sla_breached = 0;

