CREATE TABLE IF NOT EXISTS `oe_ipp_contractor_defaults` (
  id TEXT PRIMARY KEY,
  participant_id TEXT NOT NULL,
  project_id TEXT NOT NULL,
  contract_value_zar REAL NOT NULL,
  contract_tier TEXT NOT NULL CHECK(contract_tier IN ('minor','moderate','significant','major','material')),
  default_category TEXT NOT NULL CHECK(default_category IN ('insolvency','material_breach','programme_delay','quality_failure','abandonment','force_majeure_related')),
  contractor_name TEXT,
  contractor_reference TEXT,
  description TEXT,
  chain_status TEXT NOT NULL DEFAULT 'default_identified',
  sla_due_at TEXT,
  sla_breached INTEGER NOT NULL DEFAULT 0,
  default_confirmed_at TEXT,
  termination_issued_at TEXT,
  handover_completed_at TEXT,
  replacement_appointed_at TEXT,
  settlement_agreed_at TEXT,
  withdrawn_at TEXT,
  created_at TEXT NOT NULL,
  updated_at TEXT NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_ipp_cd_participant
  ON oe_ipp_contractor_defaults(participant_id);

CREATE INDEX IF NOT EXISTS idx_ipp_cd_project
  ON oe_ipp_contractor_defaults(project_id);

CREATE INDEX IF NOT EXISTS idx_ipp_cd_status
  ON oe_ipp_contractor_defaults(chain_status);

CREATE INDEX IF NOT EXISTS idx_ipp_cd_sla
  ON oe_ipp_contractor_defaults(sla_due_at)
  WHERE sla_breached = 0;

