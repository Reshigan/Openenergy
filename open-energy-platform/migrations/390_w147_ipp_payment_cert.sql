-- W147: IPP Payment Certificates
-- JBCC 6.2 Cl.40-43 + NEC4 Cl.51
-- INVERTED SLA: larger claim = more review time
-- SIGNATURE: refer_adjudication EVERY tier; certify_final crosses major/material

CREATE TABLE IF NOT EXISTS oe_ipp_payment_certs (
  id                    TEXT PRIMARY KEY,
  participant_id        TEXT NOT NULL,
  project_id            TEXT NOT NULL,
  cert_number           TEXT NOT NULL,
  claim_type            TEXT NOT NULL CHECK(claim_type IN (
                          'progress','retention_release','final_account',
                          'variation','dayworks','loss_and_expense','advance_payment')),
  value_tier            TEXT NOT NULL CHECK(value_tier IN (
                          'minor','moderate','significant','major','material')),
  claimed_value_zar     REAL NOT NULL,
  certified_value_zar   REAL,
  period_from           TEXT,
  period_to             TEXT,
  description           TEXT,
  payment_due_at        TEXT,
  chain_status          TEXT NOT NULL DEFAULT 'draft' CHECK(chain_status IN (
                          'draft','submitted','assessed','certified','disputed','revised',
                          'paid','final_payment','adjudicated','withdrawn','lapsed','rejected')),
  sla_due_at            TEXT,
  sla_breached          INTEGER NOT NULL DEFAULT 0,
  -- per-state timestamps
  submitted_at          TEXT,
  assessed_at           TEXT,
  certified_at          TEXT,
  disputed_at           TEXT,
  paid_at               TEXT,
  adjudicated_at        TEXT,
  rejected_at           TEXT,
  withdrawn_at          TEXT,
  lapsed_at             TEXT,
  created_at            TEXT NOT NULL,
  updated_at            TEXT NOT NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_ipppc_cert_number ON oe_ipp_payment_certs(project_id, cert_number);
CREATE INDEX IF NOT EXISTS idx_ipppc_participant ON oe_ipp_payment_certs(participant_id);
CREATE INDEX IF NOT EXISTS idx_ipppc_project ON oe_ipp_payment_certs(project_id);
CREATE INDEX IF NOT EXISTS idx_ipppc_status ON oe_ipp_payment_certs(chain_status);
CREATE INDEX IF NOT EXISTS idx_ipppc_sla ON oe_ipp_payment_certs(sla_due_at) WHERE sla_breached = 0;

